    public void formatarCampos(){
        this.edtId.setDocument(new PadraoNumeros());
        this.edtAlcance.setDocument(new PadraoNumeros());
        this.edtConsumo.setDocument(new PadraoNumeros());
        this.edtBairro.setDocument(new PadraoLetras());
        this.txtLog.setEditable(false);
        this.atualizarLog("Programa iniciado.");
    }
    
    public boolean validarCampos(){
        if(edtId.getText().replace(" ","").equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digite o ID corretamente.");
            edtId.requestFocus();
            return false;
        }else{
            if(this.buscar(Integer.parseInt(edtId.getText())) != -1 && flagEdtSalvar == false){
                JOptionPane.showMessageDialog(rootPane, "ID já cadastrado.");
                edtId.setText("");
                edtId.requestFocus();
                return false;
            }
        }if(cbxTipo.getSelectedItem().toString().equals("<Selecione>")){
            JOptionPane.showMessageDialog(rootPane, "Por favor selecione um tipo.");
            cbxTipo.requestFocus();
            return false;
        }if(edtBairro.getText().replace(" ","").matches("{3,}")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digite o bairro corretamente.");
            edtBairro.requestFocus();
            return false;
        }if(edtAlcance.getText().replace(" ","").equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digite o alcance corretamente.");
            edtAlcance.requestFocus();
            return false;
        }if(edtConsumo.getText().replace(" ","").equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digite o consumo corretamente.");
            edtConsumo.requestFocus();
            return false;
        }
        return true;
    }
    
