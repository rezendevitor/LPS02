    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        this.habilitarBotoes(true, false, true, true, false, true, true, false);
        if(this.flagCancelar == true){
            this.resetFields();
            this.habilitarCampos(false);
        }else if(this.flagCancelar == false){
            this.btnSalvarActionPerformed(evt);
            this.resetFields();
            this.habilitarCampos(false);
        }
        this.atualizarTabela();
        this.atualizarLog("Operação cancelada.");
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int idInformado = Integer.parseInt(JOptionPane.showInputDialog("Informe o ID da rede a ser editada: "));
        int index = this.buscar(idInformado);
        this.indice = index;
        if(this.indice == -1){
            JOptionPane.showMessageDialog(rootPane, "Rede não encontrada.");
        }else if(idInformado > 0){
            this.habilitarBotoes(false, true, false, false, true, false, false, false);
            this.habilitarCampos(true);
            this.edtId.setText(this.lstUsuarios.get(index).getId()+"");
            this.cbxTipo.setSelectedItem(this.lstUsuarios.get(index).getTipo());
            this.edtBairro.setText(this.lstUsuarios.get(index).getBairro());
            this.edtAlcance.setText(this.lstUsuarios.get(index).getAlcance()+"");
            this.edtConsumo.setText(this.lstUsuarios.get(index).getConsumo()+"");
            this.flagCancelar = true;
            this.flagEdtSalvar = true;
            this.lstUsrTemp.clear();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int idInformado = Integer.parseInt(JOptionPane.showInputDialog("Informe o ID da rede a ser excluída: "));
        this.indice = this.buscar(idInformado);
        if(this.indice == -1){
            JOptionPane.showMessageDialog(rootPane, "Rede não encontrada.");
        }else if(idInformado > 0){
            this.habilitarBotoes(true, false, true, true, false, true, true, false);
            this.lstUsuarios.remove(indice);  
            this.atualizarTabela();
            this.resetFields();
            this.habilitarCampos(false);
            this.atualizarLog("Rede " + idInformado + " excluída.");
            this.lstUsrTemp.clear();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int idInformado = Integer.parseInt(JOptionPane.showInputDialog("Informe um ID: "));
        int index = this.buscar(idInformado);
        this.indice = index;
        if(idInformado < 0){
            this.atualizarLog("Operação cancelada.");
        }else if(this.indice == -1){
            JOptionPane.showMessageDialog(rootPane, "Rede não encontrada.");
        }else{
            this.habilitarBotoes(true, false, true, true, false, true, true, true);
            this.habilitarCampos(false);
            this.flagCancelar = true;
            this.flagEdtSalvar = true;
            this.atualizarLog("Busca concluída");
            this.lstUsrTemp.add(this.lstUsuarios.get(index));
            TMRede tmusrTemp = new TMRede(this.lstUsrTemp);
            grdUsuarios.setModel(tmusrTemp);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnResetarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetarActionPerformed
        this.habilitarBotoes(true, false, true, true, false, true, true, false);
        this.lstUsrTemp.clear();
        this.atualizarTabela();
    }//GEN-LAST:event_btnResetarActionPerformed

