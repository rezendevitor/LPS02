    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(validarCampos()){
            if(this.flagEdtSalvar == true && !(this.lstUsuarios.isEmpty())){
                this.lstUsuarios.remove(this.indice);
                this.atualizarLog("Usuário " + ftxCPF.getText() + " alterado.");
            }
            try {
                SimpleDateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy");
                Date dataNasc = formatoData.parse(ftxDataNasc.getText());
                String senha = new String(pswSenha.getPassword());
                Leiturista usr = new Leiturista(edtNome.getText(), ftxCPF.getText(), ftxTelefone.getText(), edtEmail.getText(),
                                        senha, cbxSexo.getSelectedItem().toString(), dataNasc, Float.parseFloat(edtSalario.getText()));
                this.lstUsuarios.add(this.indice, usr);
                this.atualizarTabela();
                this.habilitarCampos(false);  
                this.habilitarBotoes(true, false, true, true, false, true, true, false);
                if(this.flagEdtSalvar == false){
                    this.atualizarLog("Usuário " + ftxCPF.getText() + " salvo.");
                }
                this.resetFields();
            } catch (ParseException ex) {
                Logger.getLogger(FrCadastroLeiturista.class.getName()).log(Level.SEVERE, null, ex);
            }
        }        
    }//GEN-LAST:event_btnSalvarActionPerformed

