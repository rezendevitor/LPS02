    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String cpfInformado = JOptionPane.showInputDialog("Informe o CPF do usuário a ser editado (com ponto e hífen): ");
        int index = this.buscar(cpfInformado);
        this.indice = index;
        if(this.indice == -1){
            JOptionPane.showMessageDialog(rootPane, "Usuário não encontrado.");
        }else if(!cpfInformado.equals(null)){
            this.habilitarBotoes(false, true, false, false, true, false, false, false);
            this.habilitarCampos(true);
            this.edtNome.setText(this.lstUsuarios.get(index).getNome());
            this.ftxCPF.setText(this.lstUsuarios.get(index).getCpf()+"");
            this.ftxTelefone.setText(this.lstUsuarios.get(index).getTelefone());
            this.edtEmail.setText(this.lstUsuarios.get(index).getEmail());
            this.pswSenha.setText(this.lstUsuarios.get(index).getSenha());
            this.cbxSexo.setSelectedItem(this.lstUsuarios.get(index).getSexo());
            SimpleDateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy");
            Date dataNasc = this.lstUsuarios.get(index).getDataNasc();
            String dataString = formatoData.format(dataNasc);
            this.ftxDataNasc.setText(dataString);
            this.edtSalario.setText(this.lstUsuarios.get(index).getSalario()+"");
            this.flagCancelar = true;
            this.flagEdtSalvar = true;
            this.lstUsrTemp.clear();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

